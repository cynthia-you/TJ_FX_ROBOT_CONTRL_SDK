FX_DOUBLE FX_3Root(FX_DOUBLE ix)
{

#ifdef USE_SYS_MATH
	return pow(ix, 1.0 / 3.0);
#else

	FX_DOUBLE guess = ix / 3.0;
	while (1)
	{
		FX_DOUBLE error = guess * guess * guess - ix;
		if (error > -FXARM_TINYV && error < FXARM_TINYV)
		{
			return guess;
		}
		guess = guess - error / (3 * guess * guess);
	}
#endif
}

FX_DOUBLE FX_Sqrt(FX_DOUBLE x)
{
#ifdef USE_SYS_MATH
	return sqrt(x);
#else
	FX_DOUBLE a;
	FX_DOUBLE b;
	FX_DOUBLE err;
	FX_DOUBLE Factor = 1;
	FX_DOUBLE ix = x;

	if (ix < FXARM_EPS)
	{
		return 0;
	}


	while (ix>100)
	{
		Factor *= 10.0;
		ix *= 0.01;
	}


	while (ix<0.01)
	{
		Factor *= 0.1;
		ix *= 100.0;
	}




	a = ix / 2;
	b = (a + ix / a) / 2;
	err = a - b;
	while (err > FXARM_TINYV || err < -FXARM_TINYV)
	{
		a = b;
		b = 0.5 * (a + ix / a);
		err = a - b;
	}

	return b * Factor;

#endif
}


FX_DOUBLE FX_SIN_ARC(FX_DOUBLE ArcAngle)
{
#ifdef USE_SYS_MATH
	return sin(ArcAngle);
#else
	FX_DOUBLE X;
	FX_DOUBLE ITEM;
	FX_DOUBLE TSum;
	FX_DOUBLE TX;
	FX_DOUBLE TX2;
	FX_DOUBLE iAng = ArcAngle;
	FX_INT32  ItNum;
	while (iAng < -FXARM_PI)
	{
		iAng += FXARMD_2PI;
	}
	while (iAng > FXARM_PI)
	{
		iAng -= FXARMD_2PI;
	}
	X = iAng;
	ITEM = X;
	TSum = X;
	TX = X;
	TX2 = X * X;
	ItNum = 0;
	while ((ITEM > FXARM_EPS_L || ITEM < -FXARM_EPS_L) && ItNum < 20)
	{
		TX *= TX2;
		ITEM = (TX * FX_SIN_CAL_DIV[ItNum]);
		TSum += ITEM;
		ItNum ++;
	}
	return TSum;
#endif
}
